    private void jRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRemoveActionPerformed
        try {
            int linhaSelecionada = jTable1.getSelectedRow();
            if (linhaSelecionada == -1) {
                throw new Exception("Selecione um produto na tabela.");
            }

            String texto = jTextRemove.getText().trim();
            if (texto.isEmpty()) {
                throw new NumberFormatException();
            }

            int estoqueAtual = Integer.parseInt(jTable1.getValueAt(linhaSelecionada, 2).toString());
            int valorRemovido = Integer.parseInt(texto);
            int estoqueMinimo = Integer.parseInt(jTable1.getValueAt(linhaSelecionada, 3).toString());

            int novoEstoque = estoqueAtual - valorRemovido;

            if (novoEstoque < estoqueMinimo) {
                throw new Exception("Estoque não pode ser menor que o mínimo permitido (" + estoqueMinimo + ").");
            }

            jTable1.setValueAt(String.valueOf(novoEstoque), linhaSelecionada, 2);

            CadastrarProdutoDao dao = new CadastrarProdutoDao();
            CadastrarProduto produto = dao.getListaProdutos().get(linhaSelecionada);
            produto.setQuantidade(novoEstoque);

            dao.atualizarProduto(produto);

            jTextRemove.setText("");
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Digite um valor numérico válido.");
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jRemoveActionPerformed

